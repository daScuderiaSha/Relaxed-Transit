export function searchStations(query: string, limit = 8): Station[] {
  const q = query.trim().toLowerCase()
  if (!q) return UK_STATIONS.slice(0, limit)

  return UK_STATIONS.filter(
    (s) =>
      s.name.toLowerCase().includes(q) ||
      s.crs.toLowerCase().includes(q) ||
      s.region.toLowerCase().includes(q),
  ).slice(0, limit)
}

export function getStationByCrs(crs: string): Station | undefined {
  return UK_STATIONS.find((s) => s.crs === crs)
}
